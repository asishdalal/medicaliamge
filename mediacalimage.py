def visualize_3d_comparison(image, prediction, ground_truth, save_path='comparison_3d.png'):
    """
    Show multiple slices in a grid for better 3D understanding
    Args:
        image: Input image (4, H, W, D)
        prediction: Model prediction (4, H, W, D)
        ground_truth: Ground truth mask (4, H, W, D)
        save_path: Where to save the visualization
    """
    import matplotlib.pyplot as plt

    depth = image.shape[-1]
    # Select 6 evenly spaced slices
    slice_indices = [depth//7, 2*depth//7, 3*depth//7, 4*depth//7, 5*depth//7, 6*depth//7]

    fig, axes = plt.subplots(6, 3, figsize=(15, 25))

    for row, slice_idx in enumerate(slice_indices):
        # Show FLAIR modality
        axes[row, 0].imshow(image[0, :, :, slice_idx], cmap='gray')
        axes[row, 0].set_title(f'FLAIR - Slice {slice_idx}', fontsize=12)
        axes[row, 0].axis('off')

        # Show combined ground truth (all tumor regions)
        gt_combined = (ground_truth[1:, :, :, slice_idx].sum(axis=0) > 0).astype(float)
        axes[row, 1].imshow(image[0, :, :, slice_idx], cmap='gray')
        axes[row, 1].imshow(gt_combined, cmap='Reds', alpha=0.5)
        axes[row, 1].set_title(f'Ground Truth - Slice {slice_idx}', fontsize=12)
        axes[row, 1].axis('off')

        # Show combined prediction
        pred_combined = (prediction[1:, :, :, slice_idx].sum(axis=0) > 0).astype(float)
        axes[row, 2].imshow(image[0, :, :, slice_idx], cmap='gray')
        axes[row, 2].imshow(pred_combined, cmap='Greens', alpha=0.5)
        axes[row, 2].set_title(f'Prediction - Slice {slice_idx}', fontsize=12)
        axes[row, 2].axis('off')

    plt.tight_layout()
    plt.savefig(save_path, dpi=150, bbox_inches='tight')
    print(f"✓ 3D comparison saved to {save_path}")
    plt.show()
    plt.close()

