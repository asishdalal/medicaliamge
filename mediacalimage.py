def predict(model_path, data_dir, patient_id, device='cuda'):
    """
    Predict segmentation for a single patient
    Args:
        model_path: Path to saved model
        data_dir: Path to BraTS2020_TrainingData folder
        patient_id: Patient folder name (e.g., 'BraTS20_Training_001')
        device: 'cuda' or 'cpu'
    """
    device = torch.device(device if torch.cuda.is_available() else 'cpu')

    # Load model
    model = Unetr(config).to(device)
    checkpoint = torch.load(model_path, map_location=device)
    model.load_state_dict(checkpoint['model_state_dict'])
    model.eval()

    # Load patient data
    dataset = BraTSDataset(data_dir, [patient_id])
    image, mask = dataset[0]

    # Predict
    with torch.no_grad():
        image_input = image.unsqueeze(0).to(device)
        output = model(image_input)
        prediction = torch.sigmoid(output) > 0.5

    # Calculate metrics (move tensors to same device)
    output_cpu = output.cpu()
    mask_tensor = mask.unsqueeze(0)
    dice = dice_score(output_cpu, mask_tensor)
    print(f"Dice Score: {dice:.4f}")

    # Move to CPU and convert to numpy
    prediction = prediction.cpu().numpy()[0]
    mask = mask.numpy()
    image = image.numpy()

    return image, prediction, mask

