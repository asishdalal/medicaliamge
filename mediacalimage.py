class BraTSDataset(Dataset):
    def __init__(self, data_dir, patient_ids, target_size=(128, 128, 128)):
        """
        BraTS 2020 Dataset
        Args:
            data_dir: Path to BraTS2020_TrainingData folder
            patient_ids: List of patient folder names
            target_size: Target volume size (H, W, D)
        """
        self.data_dir = Path(data_dir)
        self.patient_ids = patient_ids
        self.target_size = target_size

    def __len__(self):
        return len(self.patient_ids)

    def normalize(self, volume):
        """Normalize volume to [0, 1]"""
        volume = volume.astype(np.float32)
        if volume.max() > 0:
            volume = (volume - volume.min()) / (volume.max() - volume.min())
        return volume

    def resize_volume(self, volume, target_shape):
        """Simple resize by center cropping or padding"""
        current_shape = volume.shape

        # Calculate start indices for cropping/padding
        starts = [(cs - ts) // 2 if cs > ts else 0 for cs, ts in zip(current_shape, target_shape)]
        ends = [s + ts for s, ts in zip(starts, target_shape)]

        # Create output volume
        output = np.zeros(target_shape, dtype=volume.dtype)

        # Calculate valid ranges
        for i in range(3):
            if current_shape[i] < target_shape[i]:
                starts[i] = (target_shape[i] - current_shape[i]) // 2
                ends[i] = starts[i] + current_shape[i]

        # Copy data
        if all(cs >= ts for cs, ts in zip(current_shape, target_shape)):
            # Crop
            output = volume[starts[0]:ends[0], starts[1]:ends[1], starts[2]:ends[2]]
        else:
            # Pad
            src_slices = [slice(max(0, -s), min(cs, cs - s + ts)) for s, cs, ts in zip(starts, current_shape, target_shape)]
            dst_slices = [slice(max(0, s), max(0, s) + (src_s.stop - src_s.start)) for s, src_s in zip(starts, src_slices)]
            output[dst_slices[0], dst_slices[1], dst_slices[2]] = volume[src_slices[0], src_slices[1], src_slices[2]]

        return output

    def __getitem__(self, idx):
        patient_id = self.patient_ids[idx]
        patient_path = self.data_dir / patient_id

        # Check what files exist - try both .nii.gz and .nii
        files = list(patient_path.glob("*.nii.gz")) + list(patient_path.glob("*.nii"))
        if not files:
            raise FileNotFoundError(f"No .nii or .nii.gz files found in {patient_path}")

        # Try to find files with different naming patterns
        flair_file = None
        t1_file = None
        t1ce_file = None
        t2_file = None
        seg_file = None

        for f in files:
            fname = f.name.lower()
            if 'flair' in fname:
                flair_file = f
            elif 't1ce' in fname or 't1_ce' in fname:
                t1ce_file = f
            elif 't1' in fname and 'ce' not in fname:
                t1_file = f
            elif 't2' in fname:
                t2_file = f
            elif 'seg' in fname:
                seg_file = f

        # Verify all files are found
        if not all([flair_file, t1_file, t1ce_file, t2_file, seg_file]):
            raise FileNotFoundError(f"Missing modality files in {patient_path}. Found: {[f.name for f in files]}")

        # Load all modalities
        flair = nib.load(str(flair_file)).get_fdata()
        t1 = nib.load(str(t1_file)).get_fdata()
        t1ce = nib.load(str(t1ce_file)).get_fdata()
        t2 = nib.load(str(t2_file)).get_fdata()

        # Load segmentation mask
        seg = nib.load(str(seg_file)).get_fdata()

        # Resize volumes
        flair = self.resize_volume(flair, self.target_size)
        t1 = self.resize_volume(t1, self.target_size)
        t1ce = self.resize_volume(t1ce, self.target_size)
        t2 = self.resize_volume(t2, self.target_size)
        seg = self.resize_volume(seg, self.target_size)

        # Normalize
        flair = self.normalize(flair)
        t1 = self.normalize(t1)
        t1ce = self.normalize(t1ce)
        t2 = self.normalize(t2)

        # Stack modalities: (4, H, W, D)
        image = np.stack([flair, t1, t1ce, t2], axis=0)

        # Convert seg to multi-class (4 classes: 0=background, 1=NCR/NET, 2=ED, 4=ET)
        # We'll convert to 4 channels for each class
        mask = np.zeros((4, *self.target_size), dtype=np.float32)
        mask[0] = (seg == 0).astype(np.float32)  # Background
        mask[1] = (seg == 1).astype(np.float32)  # NCR/NET
        mask[2] = (seg == 2).astype(np.float32)  # ED
        mask[3] = (seg == 4).astype(np.float32)  # ET

        return torch.FloatTensor(image), torch.FloatTensor(mask)

# ==================== LOSS FUNCTION ====================

class DiceLoss(nn.Module):
    def __init__(self, smooth=1.0):
        super().__init__()
        self.smooth = smooth

    def forward(self, pred, target):
        pred = torch.sigmoid(pred)
        intersection = (pred * target).sum(dim=(2, 3, 4))
        union = pred.sum(dim=(2, 3, 4)) + target.sum(dim=(2, 3, 4))
        dice = (2. * intersection + self.smooth) / (union + self.smooth)
        return 1 - dice.mean()

class BCEDiceLoss(nn.Module):
    def __init__(self):
        super().__init__()
        self.bce = nn.BCEWithLogitsLoss()
        self.dice = DiceLoss()

    def forward(self, pred, target):
        return self.bce(pred, target) + self.dice(pred, target)

# ==================== METRICS ====================

def dice_score(pred, target, threshold=0.5):
    """Calculate Dice score"""
    pred = torch.sigmoid(pred) > threshold
    pred = pred.float()

    intersection = (pred * target).sum(dim=(2, 3, 4))
    union = pred.sum(dim=(2, 3, 4)) + target.sum(dim=(2, 3, 4))
    dice = (2. * intersection + 1e-7) / (union + 1e-7)
    return dice.mean().item()

