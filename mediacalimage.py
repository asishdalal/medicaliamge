def visualize_prediction(image, prediction, ground_truth, slice_idx=64, save_path='prediction.png'):
    """
    Visualize middle slice of prediction
    Args:
        image: Input image (4, H, W, D)
        prediction: Model prediction (4, H, W, D)
        ground_truth: Ground truth mask (4, H, W, D)
        slice_idx: Which slice to visualize
        save_path: Where to save the visualization
    """
    import matplotlib.pyplot as plt

    fig, axes = plt.subplots(3, 4, figsize=(16, 12))

    # Show each modality
    modalities = ['FLAIR', 'T1', 'T1ce', 'T2']
    for i, modality in enumerate(modalities):
        axes[0, i].imshow(image[i, :, :, slice_idx], cmap='gray')
        axes[0, i].set_title(f'{modality}')
        axes[0, i].axis('off')

    # Show ground truth
    classes = ['Background', 'NCR/NET', 'ED', 'ET']
    for i, class_name in enumerate(classes):
        axes[1, i].imshow(ground_truth[i, :, :, slice_idx], cmap='jet')
        axes[1, i].set_title(f'GT: {class_name}')
        axes[1, i].axis('off')

    # Show predictions
    for i, class_name in enumerate(classes):
        axes[2, i].imshow(prediction[i, :, :, slice_idx], cmap='jet')
        axes[2, i].set_title(f'Pred: {class_name}')
        axes[2, i].axis('off')

    plt.tight_layout()
    plt.savefig(save_path, dpi=150, bbox_inches='tight')
    print(f"Visualization saved to {save_path}")
