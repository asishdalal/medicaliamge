# ==================== TRAINING ====================

def train_epoch(model, dataloader, criterion, optimizer, device):
    model.train()
    total_loss = 0
    total_dice = 0

    pbar = tqdm(dataloader, desc='Training')
    for images, masks in pbar:
        images = images.to(device)
        masks = masks.to(device)

        optimizer.zero_grad()
        outputs = model(images)

        loss = criterion(outputs, masks)
        loss.backward()
        optimizer.step()

        total_loss += loss.item()
        total_dice += dice_score(outputs, masks)

        pbar.set_postfix({'loss': f'{loss.item():.4f}'})

    avg_loss = total_loss / len(dataloader)
    avg_dice = total_dice / len(dataloader)
    return avg_loss, avg_dice

def validate(model, dataloader, criterion, device):
    model.eval()
    total_loss = 0
    total_dice = 0

    with torch.no_grad():
        pbar = tqdm(dataloader, desc='Validation')
        for images, masks in pbar:
            images = images.to(device)
            masks = masks.to(device)

            outputs = model(images)
            loss = criterion(outputs, masks)

            total_loss += loss.item()
            total_dice += dice_score(outputs, masks)

            pbar.set_postfix({'loss': f'{loss.item():.4f}'})

    avg_loss = total_loss / len(dataloader)
    avg_dice = total_dice / len(dataloader)
    return avg_loss, avg_dice

def train_model(data_dir, num_epochs=50, batch_size=2, lr=1e-4):
    """
    Main training function
    Args:
        data_dir: Path to BraTS2020_TrainingData folder
        num_epochs: Number of training epochs
        batch_size: Batch size (keep small for 3D data)
        lr: Learning rate
    """
    device = torch.device('cuda' if torch.cuda.is_available() else 'cpu')
    print(f"Using device: {device}")

    # Get all patient IDs
    data_path = Path(data_dir)
    patient_ids = [d.name for d in data_path.iterdir() if d.is_dir()]

    # Split dataset
    train_ids, val_ids = train_test_split(patient_ids, test_size=0.2, random_state=42)

    print(f"Training samples: {len(train_ids)}")
    print(f"Validation samples: {len(val_ids)}")

    # Create datasets
    train_dataset = BraTSDataset(data_dir, train_ids)
    val_dataset = BraTSDataset(data_dir, val_ids)

    # Create dataloaders
    train_loader = DataLoader(train_dataset, batch_size=batch_size, shuffle=True, num_workers=4)
    val_loader = DataLoader(val_dataset, batch_size=batch_size, shuffle=False, num_workers=4)

    # Create model
    model = Unetr(config).to(device)
    print(f"Model parameters: {sum(p.numel() for p in model.parameters()):,}")

    # Loss and optimizer
    criterion = BCEDiceLoss()
    optimizer = optim.Adam(model.parameters(), lr=lr)
    scheduler = optim.lr_scheduler.ReduceLROnPlateau(optimizer, mode='min', factor=0.5, patience=5)

    # Training loop
    best_dice = 0
    for epoch in range(num_epochs):
        print(f"\nEpoch {epoch+1}/{num_epochs}")

        train_loss, train_dice = train_epoch(model, train_loader, criterion, optimizer, device)
        val_loss, val_dice = validate(model, val_loader, criterion, device)

        scheduler.step(val_loss)

        print(f"Train Loss: {train_loss:.4f}, Train Dice: {train_dice:.4f}")
        print(f"Val Loss: {val_loss:.4f}, Val Dice: {val_dice:.4f}")

        # Save best model
        if val_dice > best_dice:
            best_dice = val_dice
            torch.save({
                'epoch': epoch,
                'model_state_dict': model.state_dict(),
                'optimizer_state_dict': optimizer.state_dict(),
                'val_dice': val_dice,
            }, 'best_unetr_model.pth')
            print(f"Saved best model with Dice: {val_dice:.4f}")

    return model

# ==================== PREDICTION ====================

def predict(model_path, data_dir, patient_id, device='cuda'):
    """
    Predict segmentation for a single patient
    Args:
        model_path: Path to saved model
        data_dir: Path to BraTS2020_TrainingData folder
        patient_id: Patient folder name (e.g., 'BraTS20_Training_001')
        device: 'cuda' or 'cpu'
    """
    device = torch.device(device if torch.cuda.is_available() else 'cpu')

    # Load model
    model = Unetr(config).to(device)
    checkpoint = torch.load(model_path, map_location=device)
    model.load_state_dict(checkpoint['model_state_dict'])
    model.eval()

    # Load patient data
    dataset = BraTSDataset(data_dir, [patient_id])
    image, mask = dataset[0]

    # Predict
    with torch.no_grad():
        image = image.unsqueeze(0).to(device)
        output = model(image)
        prediction = torch.sigmoid(output) > 0.5

    # Move to CPU and convert to numpy
    prediction = prediction.cpu().numpy()[0]
    mask = mask.numpy()
    image = image.cpu().numpy()[0]

    # Calculate metrics
    dice = dice_score(torch.FloatTensor(output), torch.FloatTensor(mask).unsqueeze(0))
    print(f"Dice Score: {dice:.4f}")

    return image, prediction, mask

