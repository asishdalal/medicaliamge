# ==================== MODEL ARCHITECTURE ====================

config = {
    "patch": 16,
    "dim": 768,
    "n_heads": 12,
    "layers": 12,
    "image_size": 128,
    "image_layer": 128,
    "channel": 4
}

class Deconv(nn.Module):
    def __init__(self, in_dim, out_dim):
        super().__init__()
        self.deconv = nn.ConvTranspose3d(in_dim, out_dim, kernel_size=2, stride=2, padding=0, output_padding=0)

    def forward(self, x):
        return self.deconv(x)

class Conv(nn.Module):
    def __init__(self, in_dim, out_dim, k):
        super().__init__()
        self.conv = nn.Conv3d(in_dim, out_dim, kernel_size=k, stride=1, padding=((k - 1) // 2))

    def forward(self, x):
        return self.conv(x)

class ConvBlock(nn.Module):
    def __init__(self, indim, outdim):
        super().__init__()
        self.conv = Conv(indim, outdim, 3)
        self.norm = nn.BatchNorm3d(outdim)
        self.activation = nn.ReLU()

    def forward(self, x):
        x = self.conv(x)
        x = self.norm(x)
        x = self.activation(x)
        return x

class DeconvBlock(nn.Module):
    def __init__(self, indim, outdim):
        super().__init__()
        self.deconv = Deconv(indim, outdim)
        self.conv = Conv(outdim, outdim, 3)
        self.norm = nn.BatchNorm3d(outdim)
        self.activation = nn.ReLU()

    def forward(self, x):
        x = self.deconv(x)
        x = self.conv(x)
        x = self.norm(x)
        x = self.activation(x)
        return x

class GELU(nn.Module):
    def __init__(self):
        super().__init__()

    def forward(self, x):
        return 0.5 * x * (1.0 + torch.tanh(math.sqrt(2.0 / math.pi) * (x + 0.044715 * torch.pow(x, 3.0))))

class PatchEmbeddings(nn.Module):
    def __init__(self, cfg):
        super().__init__()
        self.patch_size = cfg["patch"]
        self.img_size = cfg["image_size"]
        self.hidden_size = cfg["dim"]
        self.depth = cfg["image_layer"]
        self.no_patch = (
            (self.img_size // self.patch_size) *
            (self.img_size // self.patch_size) *
            (self.depth // self.patch_size)
        )
        self.layer = nn.Conv3d(cfg["channel"], self.hidden_size, kernel_size=self.patch_size, stride=self.patch_size)

    def forward(self, x):
        x = self.layer(x)
        x = x.flatten(2).transpose(1, 2)
        return x

class Embeddings(nn.Module):
    def __init__(self, config):
        super().__init__()
        self.patch_embed = PatchEmbeddings(config)
        self.pos_embed = nn.Parameter(torch.randn(1, self.patch_embed.no_patch, config["dim"]))

    def forward(self, x):
        x = self.patch_embed(x)
        batch_size, _, _ = x.shape
        x = self.pos_embed + x
        return x

class SelfAttention(nn.Module):
    def __init__(self, inp_dim, outdim, cfg):
        super().__init__()
        self.w_q = nn.Linear(inp_dim, outdim)
        self.w_k = nn.Linear(inp_dim, outdim)
        self.w_v = nn.Linear(inp_dim, outdim)
        self.dropout = nn.Dropout(0.1)

    def forward(self, x):
        q = self.w_q(x)
        k = self.w_k(x)
        v = self.w_v(x)
        attn = q @ k.transpose(1, 2)
        dim_k = k.shape[-1]
        attn = attn / dim_k ** 0.5
        attn_scores = torch.softmax(attn, dim=-1)
        attn_scores = self.dropout(attn_scores)
        return attn_scores @ v

class MultiHeadAttention(nn.Module):
    def __init__(self, n_heads, inp_dim, cfg):
        super().__init__()
        self.head_dim = inp_dim // n_heads
        self.n_heads = n_heads
        self.heads = nn.ModuleList([SelfAttention(inp_dim, self.head_dim, cfg) for _ in range(n_heads)])

    def forward(self, x):
        outputs = [head(x) for head in self.heads]
        x = torch.cat(outputs, dim=-1)
        return x

class MLP(nn.Module):
    def __init__(self, inp_dim, drop_rate):
        super().__init__()
        self.layer1 = nn.Linear(inp_dim, 4 * inp_dim)
        self.layer2 = nn.Linear(4 * inp_dim, inp_dim)
        self.dropout = nn.Dropout(drop_rate)
        self.activation = GELU()

    def forward(self, x):
        x = self.layer1(x)
        x = self.activation(x)
        x = self.layer2(x)
        x = self.dropout(x)
        return x

class Transformer(nn.Module):
    def __init__(self, dim, n_heads, cfg):
        super().__init__()
        self.norm1 = nn.LayerNorm(dim)
        self.norm2 = nn.LayerNorm(dim)
        self.attn = MultiHeadAttention(n_heads, dim, cfg)
        self.mlp = MLP(dim, 0.1)

    def forward(self, x):
        residue = x
        x = self.norm1(x)
        x = self.attn(x)
        x = residue + x
        residue = x
        x = self.norm2(x)
        x = self.mlp(x)
        x = residue + x
        return x

class Encoder(nn.Module):
    def __init__(self, cfg):
        super().__init__()
        self.layers = nn.ModuleList([
            Transformer(cfg["dim"], cfg["n_heads"], cfg) for _ in range(cfg["layers"])
        ])
        self.embedding = Embeddings(cfg)

    def forward(self, x, intermidiate=True):
        x = self.embedding(x)
        if not intermidiate:
            for layer in self.layers:
                x = layer(x)
            return x
        outputs = {}
        for i, layer in enumerate(self.layers):
            x = layer(x)
            if (i + 1) % 3 == 0:
                outputs[f'z{i+1}'] = x
        return outputs

class Unetr(nn.Module):
    def __init__(self, cfg):
        super().__init__()
        self.series1 = nn.Sequential(
            ConvBlock(4, 32),
            ConvBlock(32, 64)
        )
        self.series2 = nn.Sequential(
            DeconvBlock(cfg["dim"], 512),
            DeconvBlock(512, 256),
            DeconvBlock(256, 128)
        )
        self.series3 = nn.Sequential(
            DeconvBlock(cfg["dim"], 512),
            DeconvBlock(512, 256),
        )
        self.series4 = nn.Sequential(
            DeconvBlock(cfg["dim"], 512),
        )
        self.series5 = Deconv(cfg["dim"], 512)
        self.series6 = nn.Sequential(
            ConvBlock(1024, 512),
            ConvBlock(512, 512)
        )
        self.series7 = Deconv(512, 256)
        self.series8 = nn.Sequential(
            ConvBlock(512, 256),
            ConvBlock(256, 256)
        )
        self.series9 = nn.Sequential(
            DeconvBlock(256, 128),
        )
        self.series10 = nn.Sequential(
            ConvBlock(256, 128),
            ConvBlock(128, 128),
        )
        self.series11 = Deconv(128, 64)
        self.series12 = nn.Sequential(
            ConvBlock(128, 64),
            ConvBlock(64, 16),
            nn.Conv3d(16, 4, kernel_size=1, padding=0)
        )
        self.encoder = Encoder(config)
        self.cfg = cfg

    def forward(self, x):
        residue = self.series1(x)
        outs = self.encoder(x)
        z3 = outs["z3"].transpose(1, 2).view(-1, 768, self.cfg["image_layer"]//16, self.cfg["image_size"]//16, self.cfg["image_size"]//16)
        z6 = outs["z6"].transpose(1, 2).view(-1, 768, self.cfg["image_layer"]//16, self.cfg["image_size"]//16, self.cfg["image_size"]//16)
        z9 = outs["z9"].transpose(1, 2).view(-1, 768, self.cfg["image_layer"]//16, self.cfg["image_size"]//16, self.cfg["image_size"]//16)
        z12 = outs["z12"].transpose(1, 2).view(-1, 768, self.cfg["image_layer"]//16, self.cfg["image_size"]//16, self.cfg["image_size"]//16)

        z12 = self.series5(z12)
        z9 = self.series4(z9)
        z19 = torch.cat([z9, z12], dim=1)
        t6 = self.series6(z19)
        t7 = self.series7(t6)
        z6 = self.series3(z6)
        t8 = torch.cat([z6, t7], dim=1)
        t9 = self.series8(t8)
        t9 = self.series9(t9)
        z3 = self.series2(z3)
        t10 = torch.cat([z3, t9], dim=1)
        t11 = self.series10(t10)
        t11 = self.series11(t11)
        t12 = torch.cat([residue, t11], dim=1)
        t13 = self.series12(t12)
        return t13

